    private void buscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarClienteActionPerformed
        String idStr = txtIdCliente.getText().trim();
    if (idStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ingresá un ID de cliente.");
        return;
    }
    try {
        int idCliente = Integer.parseInt(idStr);
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tablaHistorialCliente.getModel();
        modelo.setRowCount(0);
        List<ReporteCliente> lista = reporteDAO.historialDeCliente(idCliente);
        for (ReporteCliente r : lista) {
            modelo.addRow(new Object[]{r.getNombreCliente(), r.getNumeroPc(), r.getFechaSesion(), r.getMinutosUsados(), "$" + r.getTotalPagado()});
        }
        if (lista.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontraron sesiones para ese cliente.");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "El ID debe ser un número.");
    }
    }//GEN-LAST:event_buscarClienteActionPerformed

