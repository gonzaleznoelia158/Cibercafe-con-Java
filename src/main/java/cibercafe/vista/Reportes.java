    private void cargarInformePCs() {
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tablaInformes.getModel();
    modelo.setRowCount(0);
    List<InformePC> lista = reporteDAO.estadisticasUsoPCs();
    for (InformePC i : lista) {
        modelo.addRow(new Object[]{i.getNumeroPc(), i.getCantidadAlquileres(), i.getTotalMinutos(), "$" + i.getIngresosGenerados()});
    }
}

