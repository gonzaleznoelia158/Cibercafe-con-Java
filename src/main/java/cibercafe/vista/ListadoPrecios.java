    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
        String nombre = javax.swing.JOptionPane.showInputDialog(this, "Ingrese el Nombre:");
        if (nombre == null || nombre.trim().isEmpty()) return;

        String stockStr = javax.swing.JOptionPane.showInputDialog(this, "Ingrese el Stock:");
        if (stockStr == null) return;

        String precioStr = javax.swing.JOptionPane.showInputDialog(this, "Ingrese el Precio:");
        if (precioStr == null) return;

        int stock = Integer.parseInt(stockStr);
        double precio = Double.parseDouble(precioStr);

        Producto p = new Producto();
        p.setNombre(nombre.trim());
        p.setStock(stock);
        p.setPrecio(precio);
        productoDAO.insertar(p);

        cargarTabla();
        javax.swing.JOptionPane.showMessageDialog(this, "Producto agregado con éxito!");

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: El stock y precio deben ser valores numéricos");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tablaProductos.getModel();
    int filaSeleccionada = tablaProductos.getSelectedRow();

    if (filaSeleccionada != -1) {
        int id = (int) modelo.getValueAt(filaSeleccionada, 0);
        String nombreActual = modelo.getValueAt(filaSeleccionada, 1).toString();
        String stockActual = modelo.getValueAt(filaSeleccionada, 2).toString();
        String precioActual = modelo.getValueAt(filaSeleccionada, 3).toString();

        String nuevoNombre = javax.swing.JOptionPane.showInputDialog(this, "Nuevo nombre:", nombreActual);
        if (nuevoNombre == null) return;

        String nuevoStockStr = javax.swing.JOptionPane.showInputDialog(this, "Nuevo stock:", stockActual);
        if (nuevoStockStr == null) return;

        String nuevoPrecioStr = javax.swing.JOptionPane.showInputDialog(this, "Nuevo precio:", precioActual);
        if (nuevoPrecioStr == null) return;

        try {
            Producto p = new Producto();
            p.setId(id);
            p.setNombre(nuevoNombre.trim());
            p.setStock(Integer.parseInt(nuevoStockStr));
            p.setPrecio(Double.parseDouble(nuevoPrecioStr));
            productoDAO.actualizar(p);

            cargarTabla();
            javax.swing.JOptionPane.showMessageDialog(this, "Producto modificado correctamente.");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: el stock y precio deben ser valores numéricos.");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un producto de la tabla para modificar");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      int filaSeleccionada = tablaProductos.getSelectedRow();

    if (filaSeleccionada != -1) {
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tablaProductos.getModel();
        int id = (int) modelo.getValueAt(filaSeleccionada, 0);
        String nombre = modelo.getValueAt(filaSeleccionada, 1).toString();

        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this,
                "Estás seguro de que deseas eliminar " + nombre + "?",
                "Confirmar eliminación",
                javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            productoDAO.desactivar(id);
            cargarTabla();
            javax.swing.JOptionPane.showMessageDialog(this, "Producto eliminado correctamente.");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona un producto de la tabla para eliminar.");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

