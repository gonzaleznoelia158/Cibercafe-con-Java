    private void cargarTabla() {
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tablaProductos.getModel();
    modelo.setRowCount(0);
    List<Producto> lista = productoDAO.obtenerTodos();
    for (Producto p : lista) {
        modelo.addRow(new Object[]{p.getId(), p.getNombre(), p.getStock(), p.getPrecio()});
    }
    }

